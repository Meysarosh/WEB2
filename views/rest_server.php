<h1>RESTful Szerver</h1>
<p>Ez az oldal a RESTful szolgáltatás elérési útjait mutatja be az adatbázis táblákhoz:</p>
<ul>
    <li><strong>GET</strong>: Összes felhasználó vagy adott ID alapján egy felhasználó lekérése.</li>
    <li><strong>POST</strong>: Új felhasználó hozzáadása.</li>
    <li><strong>PUT</strong>: Meglévő felhasználó adatainak módosítása.</li>
    <li><strong>DELETE</strong>: Felhasználó törlése ID alapján.</li>
</ul>
<p>A szolgáltatás tesztelhető Postman vagy cURL segítségével.</p>
